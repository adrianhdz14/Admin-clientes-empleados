    //método para mostrar datos, con parámetro tipo string, valor va a recibir lo ingresado en el campo de texto de buscar
    void mostrardatos(String valor){
       
    DefaultTableModel modelo= new DefaultTableModel();//import javax.swing.table.DefaultTableModel;
    //Nombre de las columnas
    modelo.addColumn("Empleados");
    modelo.addColumn("Nombres");
    modelo.addColumn("Apellidos");
    modelo.addColumn("Email");
    modelo.addColumn("Dirección");
    modelo.addColumn("Telefono 1");
    modelo.addColumn("Telefono 2");
    modelo.addColumn("Salario");
     modelo.addColumn("Notas"); 
    //imprime los títulos de la columna
    tbempleados.setModel(modelo);
    
    //acumular la sentencia que se quiere ejecutar.
    String sql="";//variable
        //si el campo este vacío (valor)
    if(valor.equals(""))
    {
        sql="SELECT * FROM empleados";
    }
    //si no
    else{
        sql="SELECT * FROM empleados WHERE empleado_id='"+valor+"'";//cliente_id
    }
    
    //vector
    String []datos = new String [10];
        try {
            Statement st = cn.createStatement();//objeto-- pide ponerlo en try-catch (automatico)
            ResultSet rs = st.executeQuery(sql);//almacena los datos de la consulta
            //para mostrar los datos
            while(rs.next()){
                //columnas
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                datos[6]=rs.getString(7);
                datos[7]=rs.getString(8);
                datos[8]=rs.getString(9);
                //filas
                modelo.addRow(datos);
            }
            //mostrarlo en nuestra tabla
            tbempleados.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Empleados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

